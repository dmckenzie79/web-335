# add, subtract and divide functions 
# ============================================

# add function
def add (x, y):
    return x + y
print (add(1, 2))

# subtract function
def subtract (x, y):
    return x - y
print (subtract(4, 1))

# divide function
def divide (x, y):
    return x / y
print (divide(8, 2))
